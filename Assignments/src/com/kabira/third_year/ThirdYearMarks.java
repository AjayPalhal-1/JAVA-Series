package com.kabira.third_year;

public class ThirdYearMarks {

	int theoryMarks;
	int practicalMarks;

	public ThirdYearMarks(int theoryMarks, int practicalMarks) {
		super();
		this.theoryMarks = theoryMarks;
		this.practicalMarks = practicalMarks;
	}

	public int getTheoryMarks() {
		return theoryMarks;
	}

	public void setTheoryMarks(int theoryMarks) {
		this.theoryMarks = theoryMarks;
	}

	public int getPracticalMarks() {
		return practicalMarks;
	}

	public void setPracticalMarks(int practicalMarks) {
		this.practicalMarks = practicalMarks;
	}

}
