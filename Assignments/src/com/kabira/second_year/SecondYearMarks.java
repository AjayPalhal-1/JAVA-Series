package com.kabira.second_year;

public class SecondYearMarks {

	int computerMarks;
	int mathMark;
	int entcMark;

	public SecondYearMarks(int computerMarks, int mathMark, int entcMark) {
		super();
		this.computerMarks = computerMarks;
		this.mathMark = mathMark;
		this.entcMark = entcMark;
	}

	public int getComputerMarks() {
		return computerMarks;
	}

	public void setComputerMarks(int computerMarks) {
		this.computerMarks = computerMarks;
	}

	public int getMathMark() {
		return mathMark;
	}

	public void setMathMark(int mathMark) {
		this.mathMark = mathMark;
	}

	public int getEntcMark() {
		return entcMark;
	}

	public void setEntcMark(int entcMark) {
		this.entcMark = entcMark;
	}

}
