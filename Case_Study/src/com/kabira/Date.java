package com.kabira;

public class Date {
	
	int year;
	int month ;
	int tday;
	public Date(int year, int month, int tday) {
		super();
		this.year = year;
		this.month = month;
		this.tday = tday;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getTday() {
		return tday;
	}
	public void setTday(int tday) {
		this.tday = tday;
	}
	
	
	

}
